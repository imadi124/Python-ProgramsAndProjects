#17.Write a program that will take three digits from the user and add the square of each digit.
num1 = int(input("Enter the first digit :"))
num2 = int(input("Enter the Second digit :"))
num3 = int(input("Enter the Third digit :"))

sum = (num1**2)+(num2**2)+(num3**2)
print(f"The sum of the square of each digit is {sum}")
